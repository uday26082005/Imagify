import React, { useContext, useState } from 'react'
import { assets } from '../assets/assets'
import { AppContext } from '../context/AppContext'
import { motion } from 'framer-motion'
import { toast } from 'react-toastify'

const Result = () => {

  const [input, setInput] = useState('')
  const [loading, setLoading] = useState(false)
  const [isImageLoaded, setIsImageLoaded] = useState(false)
  const [image, setImage] = useState(assets.sample_img_9)
  const [mousePos, setMousePos] = useState({ x: 0, y: 0 })

  const handleMouseMove = (e) => {
    const x = (e.clientX / window.innerWidth) * 2 - 1;
    const y = (e.clientY / window.innerHeight) * 2 - 1;
    setMousePos({ x, y });
  };

  const { generateImage } = useContext(AppContext)

  const handleShare = async () => {
    try {
      if (!navigator.share) {
        toast.error("Sharing is not supported on this browser.");
        return;
      }
      
      let shareData = {
        title: 'Generated by Pixora',
        text: 'Check out this awesome image I generated on Pixora!'
      };

      if (image.startsWith('data:image')) {
        const response = await fetch(image);
        const blob = await response.blob();
        const file = new File([blob], 'pixora-image.png', { type: blob.type });
        if (navigator.canShare && navigator.canShare({ files: [file] })) {
          shareData.files = [file];
        }
      }

      await navigator.share(shareData);
    } catch (error) {
      if (error.name !== 'AbortError') {
        toast.error("Failed to share image.");
      }
    }
  };

  const onSubmitHandler = async (e) => {
    e.preventDefault()
    setLoading(true)

    if (input) {
      const image = await generateImage(input)
      if (image) {
        setIsImageLoaded(true)
        setImage(image)
      }
    }
    setLoading(false)
  }

  return (
    <motion.form 
      onSubmit={onSubmitHandler} 
      onMouseMove={handleMouseMove}
      className='flex flex-col min-h-[90vh] justify-center items-center relative'
      initial={{ opacity: 0.2, y: 100 }}
      transition={{ duration: 1 }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true }}
    >
      {/* Ambient background glow */}
      <div className={`absolute top-1/2 left-1/2 -translate-x-1/2 -translate-y-1/2 w-[60vw] max-w-[600px] h-[60vw] max-h-[600px] bg-blue-500/20 dark:bg-purple-500/20 rounded-full blur-[80px] -z-10 transition-all duration-1000 pointer-events-none ${loading ? 'opacity-100 scale-125 animate-pulse' : 'opacity-30 scale-100'}`}></div>

      <div className="glass-effect border border-white dark:border-white/10 p-8 sm:p-12 rounded-3xl flex flex-col items-center mt-10 relative z-10 shadow-[0_0_50px_rgba(0,0,0,0.1)] dark:shadow-[0_10px_50px_rgba(0,0,0,0.5)] w-[90vw] max-w-4xl">
        <div style={{ perspective: '1000px' }}>
          <div 
            className='relative shadow-2xl rounded' 
            style={{ 
              transform: `rotateX(${-mousePos.y * 15}deg) rotateY(${mousePos.x * 15}deg)`,
              transition: 'transform 0.1s ease-out',
              transformStyle: 'preserve-3d'
            }}
          >
            <img className='max-w-sm rounded' src={image} alt="" style={{ transform: 'translateZ(20px)' }} />
            <span className={`absolute bottom-0 left-0 h-1 bg-blue-500 transition-all ease-out ${loading ? 'w-[95%] duration-[15s]' : 'w-0 duration-0'}`} style={{ transform: 'translateZ(30px)' }} />
          </div>
          <p className={!loading ? 'hidden' : 'dark:text-white mt-4 text-center'}>Loading.....</p>
        </div>

        {!isImageLoaded && <div className='flex w-full max-w-2xl bg-white dark:bg-zinc-900 border border-neutral-200 dark:border-neutral-800 shadow-[0_8px_30px_rgba(0,0,0,0.08)] dark:shadow-[0_8px_30px_rgba(0,0,0,0.5)] text-black dark:text-white text-sm p-1 mt-10 rounded-full'>
          <input onChange={e => setInput(e.target.value)} value={input} className='flex-1 bg-transparent outline-none ml-8 pr-4 min-w-0 text-gray-700 dark:text-white placeholder-gray-500 dark:placeholder-gray-300' type="text" placeholder='Describe what you want to generate' />
          <button type='submit' className='bg-zinc-900 text-white dark:bg-white dark:text-black px-10 sm:px-16 py-3 rounded-full'>Generate</button>
        </div>}

        {isImageLoaded && <div className='flex gap-2 flex-wrap justify-center text-white text-sm p-0.5 mt-10 rounded-full'>
          <p onClick={() => { setIsImageLoaded(false) }} className='bg-transparent border border-zinc-900 dark:border-white text-black dark:text-white px-8 py-3 rounded-full cursor-pointer hover:bg-neutral-100 dark:hover:bg-neutral-800 transition-colors'>Generate Another</p>
          <a href={image} download className='bg-zinc-900 dark:bg-white dark:text-black px-10 py-3 rounded-full cursor-pointer hover:bg-zinc-800 dark:hover:bg-gray-200 transition-colors'>Download</a>
          <p onClick={handleShare} className='bg-blue-600 dark:bg-blue-500 text-white px-10 py-3 rounded-full cursor-pointer hover:bg-blue-700 dark:hover:bg-blue-600 transition-colors'>Share</p>
        </div>}
      </div>

    </motion.form>
  )
}

export default Result